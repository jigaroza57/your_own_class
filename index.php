                <script>
                    function openLocalVideo(src) {
                        document.getElementById('modalContent').innerHTML =
                            '<video src="' + src + '" controls autoplay style="width:100%;height:100%;object-fit:contain;"></video>';
                        document.getElementById('videoModal').style.display = 'flex';
                        document.body.style.overflow = 'hidden';
                    }

                    function closeModal(e) {
                        if (!e || e.target.id === 'videoModal') {
                            document.getElementById('modalContent').innerHTML = '';
                            document.getElementById('videoModal').style.display = 'none';
                            document.body.style.overflow = '';
                        }
                    }

                    function scrollReviews(amount) {
                        document.getElementById('reviewScroll').scrollBy({
                            left: amount,
                            behavior: 'smooth'
                        });
                    }

                    document.addEventListener('keydown', function(e) {
                        if (e.key === 'Escape') closeModal();
                    });
                </script>
