                <script>
                    const slider = document.getElementById('oeCourseSlider');
                    const nextBtn = document.getElementById('oeNext');
                    const prevBtn = document.getElementById('oePrev');

                    function getScrollAmount() {

                        const card = slider.querySelector('.oe-course-box');
                        const cardWidth = card.offsetWidth;
                        const style = window.getComputedStyle(slider);
                        const gap = parseInt(style.columnGap) || 20;

                        return cardWidth + gap;

                    }

                    nextBtn.addEventListener('click', () => {

                        slider.scrollBy({
                            left: getScrollAmount(),
                            behavior: 'smooth'
                        });

                    });

                    prevBtn.addEventListener('click', () => {

                        slider.scrollBy({
                            left: -getScrollAmount(),
                            behavior: 'smooth'
                        });

                    });
                </script>

